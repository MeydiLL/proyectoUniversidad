    private String generarMatricula(String curp){
        String matricula="";
        for(int i=0; i<=9; i++){
            matricula+=curp.charAt(i);
        }
        return matricula;
    }
    private String generarPassword(){
    /*Hay que recordar que la contrase単a es un varchar de 8*/
    String password="";
    Random random = new Random();
    String cadena = "abcdefghijklmn単opqrstuvwxyz0123456789";
    
    for(int i=0; i<=7; i++){
        int randomInt = random.nextInt(cadena.length());
        password+= cadena.charAt(randomInt);
    }
        
    
    return password;
    }
    private int getIdInscripcion(String name, String curp){
        
        int a = 0;        
        String sql = String.format("SELECT Id_Inscripcion FROM `inscripciones` WHERE Nombre='%s' and CurpString='%s'", name, curp);       
        ResultSet rs = conexion.consultarRegistros(sql);
        try {           
            while(rs.next()){
                a = Integer.parseInt(rs.getString("Id_Inscripcion"));
            }
            //tbGrado.setModel(modelo);
        } catch (Exception e) {
        }
        return a;
    }
