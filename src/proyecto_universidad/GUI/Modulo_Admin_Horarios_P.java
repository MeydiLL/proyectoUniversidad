    void mostrarDatos() {
        String sql = "select Id_grupo, Dia, Hora , Aula , asignaturas.Nombre, profesores.Nombre  from horario_alumno,grupos, asignaturas, profesores where Grupos_Id_Grupo=Id_Grupo and Asignaturas_Id_Asignatura= Id_Asignatura and horario_alumno.Profesores_Matricula_profesor=profesores.Matricula_profesor";
        ResultSet rs = conexion.consultarRegistros(sql);
        try {
            Object[] horario = new Object[9];
            modelo = (DefaultTableModel) tbHorario.getModel();
            while (rs.next()) {
                horario[0] = rs.getString("Id_Grupo");
                horario[1] = rs.getString("asignaturas.Nombre");
                horario[2] = rs.getString("profesores.Nombre");
                horario[3] = rs.getString("Aula");
                
                /*Se crea una variable auxiliar para almacenar el nombre del primer dia*/
                String aux = rs.getString("Dia");        
                
                
                if(aux.toString().equals("Lunes")){
                    horario[4] = rs.getString("Hora");
                    horario[5] = "";
                    horario[6] = rs.getString("Hora");
                    horario[7] = "";
                    horario[8] = rs.getString("Hora");
                }else{
                    horario[4] = "";
                    horario[5] = rs.getString("Hora");
                    horario[6] = "";
                    horario[7] = rs.getString("Hora");
                    horario[8] = "";
                }
                modelo.addRow(horario);
            }
            //tbGrado.setModel(modelo);
        } catch (Exception e) {
        }
    }
